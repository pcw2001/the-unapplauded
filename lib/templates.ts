export function getRandomItem<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

export function generateTitle(gallery: GalleryType): string {
  return getRandomItem(templates[gallery].titles);
}

export function generateLabel(gallery: GalleryType): string {
  return getRandomItem(templates[gallery].labels);
}

export function generateCuratorNote(gallery: GalleryType): string {
  return getRandomItem(templates[gallery].curatorNotes);
}

export function generateMaterials(gallery: GalleryType): string {
  return getRandomItem(templates[gallery].materials);
}
